# quando ultilizamos um operando dizemos que o operador é Unário

# quando ultilizamos dois operandos dizemos que o operador é Binário

#--------------------------------------------------------------------#

# Operador NOT (Unário)

print(not True)

print(not False)


#----------------------------------------------------------------------#

# Operador AND (Binário)

print(True and True)
print(True and False)
print(False and True)
print(False and False)

# Só tem sua tabela verdadeira se os dois componentes são verdadeiros
# Predominante o False

#----------------------------------------------------------------------#

# Operador OR (Binário)

print(True or True)
print(True or False)
print(False or True)
print(False or False)

# Tem sua tabela verdadeira quando um componente é verdadeiro
# Predominante o True

